/**
 * ملف: emoji.types.ts
 * الغرض: تعريفات الأنواع (TypeScript Types) الخاصة بوحدة لعبة "خمّن
 * الشخصية من الإيموجي" (Emoji Game) فقط. هذا الملف مسؤول فقط عن "شكل"
 * بيانات السؤال، ولا يحتوي أي منطق تنفيذي - بنفس نمط riddles.types.ts
 * وeye.types.ts تمامًا.
 */

/**
 * شكل سؤال "إيموجي" الواحد كما يُخزَّن في questions.json وكما يُرجَع في
 * استجابات الـ API.
 *
 * - id: رقم صحيح موجب فريد يميّز كل سؤال. **مخزَّن مسبقًا داخل
 *   questions.json نفسه** (نفس أسلوب riddles.types.ts وeye.types.ts)،
 *   مرقّم تسلسليًا (1، 2، 3...) حسب ترتيب السؤال في الملف وقت إنشائه.
 *   عند إضافة أسئلة جديدة، يجب إعطاء كل سؤال id فريدًا لم يُستخدم من
 *   قبل (عادة: أكبر id موجود حاليًا + 1). هذا الحقل مهم جدًا لأن بوت
 *   الواتساب يعتمد عليه في نظام منع التكرار (anti-repeat) عبر إرسال
 *   قائمة الـ id المستخدمة سابقًا إلى `random-exclude`.
 * - emoji: نص يحتوي مجموعة الإيموجيات التي تمثّل الشخصية (يدعم UTF-8
 *   بالكامل، بما في ذلك تسلسلات الإيموجي المركّبة مثل الأعلام أو
 *   ZWJ sequences).
 * - category: تصنيف السؤال (مثل اسم الأنمي)، ويمكن استخدامه مستقبلًا
 *   لأي تصنيف آخر غير الأنمي. هذا الحقل يحل محل حقل "anime" الذي كان
 *   موجودًا في البيانات الخام الأصلية.
 * - answers: مصفوفة الإجابات الصحيحة المقبولة لهذا السؤال (تدعم
 *   العربية والإنجليزية وأي مرادفات مطلوبة في نفس المصفوفة). سؤال واحد
 *   قد يقبل أكثر من صياغة صحيحة لنفس الإجابة (مثل "لوفي" و"luffy")،
 *   لذلك هذا الحقل مصفوفة نصوص دائمًا، وليس نصًا مفردًا أبدًا - بنفس
 *   أسلوب answers في riddles.types.ts وquiz.types.ts.
 */
export interface EmojiQuestion {
  id: number;
  emoji: string;
  category: string;
  answers: string[];
}
