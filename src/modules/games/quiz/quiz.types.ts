/**
 * ملف: quiz.types.ts
 * الغرض: تعريفات الأنواع (TypeScript Types) الخاصة بوحدة الكويز فقط.
 *
 * هذا الملف مسؤول فقط عن "شكل" بيانات سؤال الكويز، ولا يحتوي أي منطق
 * تنفيذي. أي وحدة لعبة مستقبلية (anime، riddles...) يجب أن يكون لها
 * ملف types خاص بها بنفس هذا النمط، منفصل تمامًا عن أنواع الألعاب
 * الأخرى.
 */

/**
 * شكل سؤال الكويز الواحد كما يُخزَّن في questions.json وكما يُرجَع في
 * استجابات الـ API.
 *
 * - id: رقم فريد يميّز كل سؤال (لا يتكرر بين الأسئلة).
 * - question: نص السؤال.
 * - answer: الإجابة الصحيحة.
 * - category: تصنيف موضوعي اختياري للسؤال (مثل "جغرافيا"، "علوم"...).
 * - difficulty: مستوى الصعوبة، واحدة من ثلاث قيم محددة فقط.
 */
export interface QuizQuestion {
  id: number;
  question: string;
  answer: string;
  category: string;
  difficulty: "easy" | "medium" | "hard";
}
