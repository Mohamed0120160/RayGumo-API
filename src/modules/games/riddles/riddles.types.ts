/**
 * ملف: riddles.types.ts
 * الغرض: تعريفات الأنواع (TypeScript Types) الخاصة بوحدة الألغاز فقط.
 * هذا الملف مسؤول فقط عن "شكل" بيانات اللغز، ولا يحتوي أي منطق تنفيذي —
 * بنفس نمط quiz.types.ts وtrue-false.types.ts تمامًا.
 */

/**
 * شكل اللغز الواحد كما يُخزَّن في questions.json وكما يُرجَع في
 * استجابات الـ API.
 *
 * - id: رقم صحيح موجب فريد يميّز كل لغز. **مخزَّن مسبقًا داخل
 *   questions.json نفسه** (نفس أسلوب true-false.types.ts)، مرقّم
 *   تسلسليًا (1، 2، 3...) حسب ترتيب اللغز في الملف وقت إنشائه. عند
 *   إضافة ألغاز جديدة، يجب إعطاء كل لغز id فريدًا لم يُستخدم من قبل
 *   (عادة: أكبر id موجود حاليًا + 1). هذا الحقل مهم جدًا لأن بوت
 *   الواتساب يعتمد عليه في نظام منع التكرار (anti-repeat) عبر إرسال
 *   قائمة الـ id المستخدمة سابقًا إلى `random-exclude`.
 * - question: نص اللغز (يدعم العربية وUTF-8 بالكامل).
 * - answers: مصفوفة الإجابات الصحيحة المقبولة لهذا اللغز. لغز واحد قد
 *   يقبل أكثر من صياغة صحيحة لنفس الإجابة (مثل "حفرة" و"الحفرة")، لذلك
 *   هذا الحقل مصفوفة نصوص دائمًا، وليس نصًا مفردًا أبدًا - بنفس أسلوب
 *   answers في quiz.types.ts.
 *
 * ملاحظة: لا يوجد حقل "category" أو "difficulty" في هذا الإصدار، لأن
 * مجموعة بيانات الألغاز الحقيقية المزوَّدة لا تحتوي هذه الحقول. أي
 * إضافة لها مستقبلًا يجب أن تأتي من بيانات حقيقية تحملها فعلًا وليس
 * بشكل افتراضي.
 */
export interface Riddle {
  id: number;
  question: string;
  answers: string[];
}
